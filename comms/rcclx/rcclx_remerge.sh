  slug=$(echo "$f" | tr '/' '_')
  PRE_F="$TMPDIR_MERGE/pre_${slug}"
  POST_F="$TMPDIR_MERGE/post_${slug}"
  PATCH_F="$TMPDIR_MERGE/patch_${slug}.diff"
  AUTO_PATCH_F="$TMPDIR_MERGE/auto_${slug}.diff"
  DEST="$RCCLX_WORKDIR/$f"

  echo -e "${BLUE}${BOLD}[$COUNT/$TOTAL] $f${NC}"

  # Extract file at both revisions from the same rcclx path
  sl cat -r "$PRE_REV"  "$RCCLX_SRC/$f" > "$PRE_F"  2>/dev/null || true
  sl cat -r "$POST_REV" "$RCCLX_SRC/$f" > "$POST_F" 2>/dev/null || true

  # ---- Guards -------------------------------------------------------------
  if [ ! -s "$PRE_F" ] && [ ! -s "$POST_F" ]; then
    echo -e "  ${YELLOW}SKIP — absent at both revisions${NC}"
    SKIPPED=$((SKIPPED+1)); SKIPPED_FILES+=("$f")
    echo "SKIP: $f" >> "$LOG_FILE"; continue
  fi

  if [ ! -s "$PRE_F" ]; then
    echo -e "  ${CYAN}NEW — added by feb drop; keeping as-is${NC}"
    IDENTICAL=$((IDENTICAL+1)); echo "NEW_FILE: $f" >> "$LOG_FILE"; continue
  fi

  if [ ! -s "$POST_F" ]; then
    echo -e "  ${YELLOW}DELETED — feb drop removed it; restoring from PRE${NC}"
    if ! $DRY_RUN; then mkdir -p "$(dirname "$DEST")"; cp "$PRE_F" "$DEST"; fi
    AUTO=$((AUTO+1)); echo "RESTORED_DELETED: $f" >> "$LOG_FILE"; continue
  fi

  # ---- No diff → nothing to do -------------------------------------------
  if diff -q "$PRE_F" "$POST_F" > /dev/null 2>&1; then
    echo -e "  ${GREEN}IDENTICAL — no customizations to restore${NC}"
    IDENTICAL=$((IDENTICAL+1)); echo "IDENTICAL: $f" >> "$LOG_FILE"; continue
  fi

  # ---- Generate diff(POST → PRE):
  #      (+) lines = Meta customizations to add back
  #      (-) lines = upstream feb drop additions to leave alone
  diff -u "$POST_F" "$PRE_F" > "$PATCH_F" 2>/dev/null || true

  # ---- Classify hunks with embedded Python filter -------------------------
  FILTER_STDERR="$TMPDIR_MERGE/filter_${slug}.log"
  if python3 -c "$HUNK_FILTER" < "$PATCH_F" > "$AUTO_PATCH_F" 2>"$FILTER_STDERR"; then
    HAS_MIXED=false
  else
    HAS_MIXED=true
  fi
  HUNK_STATS=$(cat "$FILTER_STDERR")

  if $DRY_RUN; then
    echo -e "  ${CYAN}DRY RUN: $HUNK_STATS${NC}"
    if $HAS_MIXED; then
      echo -e "  ${YELLOW}Mixed hunks found — would need manual resolution${NC}"
    fi
    echo "DRY_RUN: $f ($HUNK_STATS)" >> "$LOG_FILE"
    echo ""; continue
  fi

  # ---- Apply the auto-patch (pure Meta customization hunks) ---------------
  cp "$POST_F" "$DEST"   # start from POST (has upstream changes)
  PATCH_REJECTS=false
  REJ_F="${DEST}.rej"

  if [ -s "$AUTO_PATCH_F" ]; then
    # Apply with fuzz=3 to tolerate minor context shifts from upstream changes
    if ! patch --fuzz=3 --quiet "$DEST" < "$AUTO_PATCH_F" 2>/dev/null; then
      PATCH_REJECTS=true
    fi
  fi

  # ---- Determine outcome --------------------------------------------------
  if ! $HAS_MIXED && ! $PATCH_REJECTS; then
    # All Meta customizations applied cleanly, no manual work needed
    echo -e "  ${GREEN}AUTO-MERGED: $HUNK_STATS${NC}"
    rm -f "$REJ_F"
    AUTO=$((AUTO+1)); echo "AUTO_MERGED: $f ($HUNK_STATS)" >> "$LOG_FILE"

  else
    # Some hunks couldn't be auto-applied — need manual resolution
    REASON=""
    $HAS_MIXED     && REASON+="mixed hunks"
    $PATCH_REJECTS && REASON+="${REASON:+, }patch rejects"
    echo -e "  ${YELLOW}PARTIAL AUTO-MERGE: $HUNK_STATS — $REASON${NC}"
    echo -e "  ${YELLOW}Opening vimdiff: LEFT=PRE (old rcclx), RIGHT=DEST (partially merged)${NC}"
    echo -e "  ${YELLOW}Copy remaining Meta customizations from LEFT into RIGHT, then :wqa${NC}"

    # Show reject summary if any
    if [ -f "$REJ_F" ]; then
      echo ""
      echo -e "  ${RED}Rejected hunks (in ${REJ_F}):${NC}"
      grep "^@@" "$REJ_F" | head -10 | sed 's/^/    /'
      echo ""
    fi

    read -rp "  Press Enter to open vimdiff, or 's' to skip, 'q' to quit: " resp
    case "$resp" in
      s|S) echo -e "  ${YELLOW}SKIPPED${NC}"; SKIPPED=$((SKIPPED+1)); SKIPPED_FILES+=("$f")
           echo "SKIPPED_MANUAL: $f" >> "$LOG_FILE"; echo ""; continue ;;
      q|Q) echo -e "${YELLOW}Quitting — resume with: --from $f${NC}"
           echo "QUIT_AT: $f" >> "$LOG_FILE"; break ;;
    esac

    $MERGE_TOOL "$PRE_F" "$DEST" || true
    rm -f "$REJ_F"
    MANUAL=$((MANUAL+1)); MANUAL_FILES+=("$f")
    echo "MANUAL_MERGED: $f ($HUNK_STATS, reason=$REASON)" >> "$LOG_FILE"
    echo -e "  ${GREEN}Done${NC}"
  fi

  echo ""
done

