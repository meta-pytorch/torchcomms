# --------------------------------------------------------------------------
# Embedded Python hunk filter
# Reads diff(POST, PRE) from stdin and:
#   - Outputs ONLY pure (+) hunks as a patch (Meta customizations to restore)
#   - Skips pure (-) hunks (upstream additions already in POST — leave alone)
#   - Marks mixed hunks as needing manual resolution
# Exit code 0 = all hunks auto-applied; 1 = mixed hunks need manual resolution
# --------------------------------------------------------------------------
HUNK_FILTER=$(cat << 'PYEOF'
import sys, re

def parse_diff(text):
    header, hunks = [], []
    cur = None
    for line in text.splitlines(keepends=True):
        if line.startswith(('--- ', '+++ ')):
            header.append(line)
        elif line.startswith('@@'):
            if cur: hunks.append(cur)
            cur = {'hdr': line, 'lines': []}
        elif cur is not None:
            cur['lines'].append(line)
    if cur: hunks.append(cur)
    return header, hunks

def classify(hunk):
    has_add = any(l.startswith('+') for l in hunk['lines'])
    has_del = any(l.startswith('-') for l in hunk['lines'])
    if has_add and not has_del: return 'add'   # Meta customization → apply
    if has_del and not has_add: return 'del'   # upstream addition  → skip
    return 'mixed'                              # conflict           → manual

header, hunks = parse_diff(sys.stdin.read())
classified = [(h, classify(h)) for h in hunks]

auto  = [h for h, k in classified if k == 'add']
skip  = [h for h, k in classified if k == 'del']
mixed = [h for h, k in classified if k == 'mixed']

# Emit filterd patch: header + only the pure-addition hunks
sys.stdout.write(''.join(header))
for h in auto:
    sys.stdout.write(h['hdr'])
    sys.stdout.write(''.join(h['lines']))

# Report to stderr
print(f"auto={len(auto)} skip(upstream)={len(skip)} manual={len(mixed)}", file=sys.stderr)

sys.exit(1 if mixed else 0)
PYEOF
)

