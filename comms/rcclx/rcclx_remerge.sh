# =============================================================================
#
# Uses the two rcclx commits to perform the merge automatically:
#
#   PRE  = parents(4acd7e79b385) — old rcclx WITH Meta customizations
#   POST = 4acd7e79b385          — new rcclx AFTER feb drop (upstream, no Meta)
#
#   diff(POST, PRE) gives:
#     (+) lines: Meta customizations that were LOST   → add back automatically
#     (-) lines: Upstream feb drop additions to KEEP  → leave alone
#
# Merge strategy for each file:
#   1. Extract pure (+) hunks (Meta customizations) from diff(POST, PRE)
#   2. Apply them as a patch to POST in the working tree
#   3. If patch applies cleanly → done (auto-merged)
#   4. If mixed hunks or patch rejects → vimdiff for manual resolution
#
# Usage:
#   bash rcclx_remerge.sh                  # process all 53 files
#   bash rcclx_remerge.sh --file <relpath> # process one file
#   bash rcclx_remerge.sh --dry-run        # show what would happen, no writes
#   bash rcclx_remerge.sh --from <file>    # resume from a specific file
#
# =============================================================================

